// Declaring Variables

var light = 'red';

switch(light) {

    case 'green':
        console.log("Drive!");
        break;
    case 'orange':
        console.log("Get Ready!");
        break;
    case 'red':
        console.log("Stop!");
        break;
    default:
        console.log("The light is not green, red or orange!");
        break;

}