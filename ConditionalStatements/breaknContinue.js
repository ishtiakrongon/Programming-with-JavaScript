// break = breaks out of loop entirely
// continue = skip an iteration of a loop

for (let i = 1; i <= 20; i++) {

    if (i === 13) {
        break;
    }

    console.log(i);

}

//------------------------------------------------------

for (let i = 1; i <= 20; i++) {

    if (i === 13) {
        continue; // it will skip iteration 13
    }

    console.log(i);


}

//----------------------------------------------------

